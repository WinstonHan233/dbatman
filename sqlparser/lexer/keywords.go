func findKeywords(b []byte, isfunc bool) (int, bool) {
	var mp map[string]int
	mp = Symbols
	if isfunc {
		mp = Functions
	}

	if v, ok := mp[string(bytes.ToUpper(b))]; ok {
		return v, true
	}

	return 0, false
}
